/*Special symbols that are used to perform actions or operations are known as operators.*/

#include<stdio.h>
int main(int argc, char const *argv[])
{
    //Arithemetic Operators
    int a=10,b=5;
    printf("%d + %d = %d\n",a,b,a+b);
    printf("%d - %d = %d\n",a,b,a-b);
    printf("%d * %d = %d\n",a,b,a*b);
    printf("%d / %d = %d\n",a,b,a/b);
    printf("%d  modulous %d = %d\n",a,b,a%b);

    //
    return 0;
}
